def evaluate_multi_output(model_name, y_test, y_pred, targets):
    for i, target in enumerate(targets):
        rmse = mean_squared_error(y_test.iloc[:, i], y_pred[:, i], squared=False)
        r2 = r2_score(y_test.iloc[:, i], y_pred[:, i])
